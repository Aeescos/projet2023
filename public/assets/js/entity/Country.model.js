export class Countries{
    Name;
    Code;
    Language;
    Capital;
    Currency;
    Country_Flag;
    Internet_Users;
    Population;

    constructor(Name, Code, Language, Capital, Currency, Country_Flag, Internet_Users, Population) {
        this.Name = Name;
        this.Code = Code;
        this.Language = Language;
        this.Capital = Capital;
        this.Currency = Currency;
        this.Country_Flag = Country_Flag;
        this.Internet_Users = Internet_Users;
        this.Population = Population;
    }
}
